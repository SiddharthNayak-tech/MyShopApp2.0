class ArithmeticOperator 
{
	public static void main(String[] args) 
	{
		int a = 250;
		int b = 672;

        //addition
		System.out.println("a + b = "+(a+b));
		 //substraction
		System.out.println("a - b = "+(a-b));
		 //multiplication
		System.out.println("a * b = "+(a*b));
		 //division
		System.out.println("a / b = "+(a/b));
		 //modulos
		System.out.println("a % b = "+(a%b));
	}
}
